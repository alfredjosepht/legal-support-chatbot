# Generate variations to reach 1000+ examples
all_data = []
for category, examples in training_data.items():
    for example in examples:
        all_data.append((example, category))

# Expand by 2.5x with variations
variations_templates = [
    "{}",
    "{} (This is happening to me regularly)",
    "I am experiencing this: {}",
    "Help! {}",
    "{} Please help me.",
    "This is what is happening: {}",
    "Someone is: {}",
    "{} What can I do?",
    "I need help because: {}",
    "The issue is: {}",
]

expanded_data = list(all_data)  # Start with original
for text, label in all_data:
    for template in variations_templates[:2]:  # 2 variations per entry
        expanded_data.append((template.format(text), label))

# Create CSV
with open('data/dataset_new.csv', 'w', newline='', encoding='utf-8') as f:
    writer = csv.writer(f)
    writer.writerow(['text', 'label'])
    for text, label in expanded_data:
        writer.writerow([text, label])

